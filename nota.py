no1 = float(input("Digite a primeira nota do aluno: "))
no2 = float(input("Digite a segunda nota do aluno: "))

calc = (no1+no2)/2     #de acordo com a procedencia (parenteses antes)

print(f"As notas foram {no1} e {no2}\nA média do aluno foi {calc:.1f}")

